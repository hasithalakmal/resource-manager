CREATE TABLE FILE_DATA(
FILE_ID int(11)NOT NULL AUTO_INCREMENT,
FILE_NAME varchar(100)NOT NULL,
FILE_DESCRIPTION varchar(300) NULL,
CATEGORY varchar(100)NOT NULL,
EVENT varchar(100)NOT NULL,
FILE_FORMAT varchar(45) NULL DEFAULT 'UNKNOWN',
GENERATED_FILE_PATH varchar(1000)NOT NULL,
EVENT_DATE varchar(100)DEFAULT NULL,
AUTH_LEVEL varchar(45)DEFAULT NULL,
STATUS varchar(45)DEFAULT NULL DEFAULT 'ACTIVE',
CREATED_TS timestamp NULL DEFAULT CURRENT_TIMESTAMP,
CREATED_BY varchar(45)DEFAULT NULL,
UPDATED_TS timestamp NULL DEFAULT CURRENT_TIMESTAMP,
UPDATED_BY varchar(45)DEFAULT NULL,
PRIMARY KEY(FILE_ID)
)ENGINE = InnoDB DEFAULT CHARSET = utf8mb4;